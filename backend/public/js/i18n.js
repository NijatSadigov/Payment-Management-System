const SUPPORTED_LANGS = ['az', 'en', 'ru'];

function currentLang() {
  const l = localStorage.getItem('lang');
  return SUPPORTED_LANGS.includes(l) ? l : 'az';
}

function localeTag() {
  return { az: 'az-AZ', en: 'en-GB', ru: 'ru-RU' }[currentLang()] || 'az-AZ';
}

// Translate a key with optional {placeholder} substitution.
function t(key, vars) {
  const lang = currentLang();
  let s = (I18N[lang] && I18N[lang][key]) || I18N.en[key] || key;
  if (vars) {
    Object.keys(vars).forEach((k) => {
      s = s.replace('{' + k + '}', vars[k]);
    });
  }
  return s;
}

// Apply translations to static markup.
function applyI18n(root) {
  const scope = root || document;
  scope.querySelectorAll('[data-i18n]').forEach((el) => {
    el.textContent = t(el.getAttribute('data-i18n'));
  });
  scope.querySelectorAll('[data-i18n-ph]').forEach((el) => {
    el.setAttribute('placeholder', t(el.getAttribute('data-i18n-ph')));
  });
  document.documentElement.lang = currentLang();
}

function setLang(l) {
  if (!SUPPORTED_LANGS.includes(l)) return;
  localStorage.setItem('lang', l);
  location.reload();
}

// Compact EN/AZ/RU switcher markup. `variant` = 'dark' (sidebar) or 'light'.
function langSwitcher(variant) {
  const cur = currentLang();
  const btns = SUPPORTED_LANGS.map(
    (l) =>
      `<button type="button" class="lang-btn ${l === cur ? 'active' : ''}" onclick="setLang('${l}')">${l.toUpperCase()}</button>`,
  ).join('');
  return `<div class="lang-switch ${variant || ''}">${btns}</div>`;
}

if (document.readyState === 'loading') {
  document.addEventListener('DOMContentLoaded', () => applyI18n());
} else {
  applyI18n();
}
